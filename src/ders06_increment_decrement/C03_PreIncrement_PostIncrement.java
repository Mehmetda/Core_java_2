package ders06_increment_decrement;

public class C03_PreIncrement_PostIncrement {
    public static void main(String[] args) {

        int sayı=10;
        sayı++;
        System.out.println(sayı);

        ++sayı;
        System.out.println(sayı);

        System.out.println(sayı++);
        System.out.println(sayı);

        System.out.println(++sayı);
        System.out.println(sayı);
    }
}
