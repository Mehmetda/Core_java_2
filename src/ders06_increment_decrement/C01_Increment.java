package ders06_increment_decrement;

public class C01_Increment {

    public static void main(String[] args) {
        int sayı=10;

        System.out.println(sayı=sayı+3);
        System.out.println(sayı);

        System.out.println(sayı-=5);
        System.out.println(sayı);

        System.out.println(sayı++);
        System.out.println(sayı);

    }
}
