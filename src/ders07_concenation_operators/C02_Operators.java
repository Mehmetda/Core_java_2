package ders07_concenation_operators;

public class C02_Operators {

    public static void main(String[] args) {

        int a=20;
        a=a+10;

        int b=20;
        System.out.println(a==b);

        System.out.println(a==b+10);

        boolean c=20==b;
        System.out.println(c);
    }
}
